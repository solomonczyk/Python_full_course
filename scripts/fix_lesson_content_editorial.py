def validate_schema(original_lessons, fixed_lessons):
    """Validate that schema is preserved."""
    assert len(original_lessons) == len(fixed_lessons), "Lesson count changed!"
    orig_ids = [les["id"] for les in original_lessons]
    fix_ids = [les["id"] for les in fixed_lessons]
    assert orig_ids == fix_ids, "Lesson IDs changed or reordered!"

    # Fields that MUST be in every lesson (from schema audit)
    REQUIRED_FIELDS = {
        "id", "part", "chapter", "lesson", "slug", "title", "subtitle",
        "difficulty", "estimated_time_min", "scene_image", "topic", "locked",
        "story_placement", "pre_topic_dialogue", "post_error_dialogue",
        "mini_summary", "connection_to_game", "game_relevance",
        "syntax_reminder", "explanation", "quiz", "what_outputs",
        "find_bug", "mission", "analogy", "practice_subtasks",
        "runnable_code", "common_mistakes",
    }

    for lesson in fixed_lessons:
        current_fields = set(lesson.keys())
        missing = REQUIRED_FIELDS - current_fields
        assert not missing, (
            f"Lesson {lesson['id']} missing required fields: {missing}"
        )

    print("Schema validation passed: all 28 required fields preserved, IDs intact, count 92.")
    return True


